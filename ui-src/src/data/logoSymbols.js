/**
 * Gets a random symbol for a given ideology.
 * @param {string} ideologyId - The ID of the ideology (e.g., 'conservative').
 * @returns {Array<Array<number>>} A 16x16 matrix for the symbol.
 */
export const getSymbolForIdeology = (ideologyId) => {
  const symbols = SYMBOLS[ideologyId] || SYMBOLS.default;
  return symbols[Math.floor(Math.random() * symbols.length)];
};

/**
 * Gets a random background pattern.
 * @returns {Array<Array<number>> | null} A 16x16 matrix for the pattern or null.
 */
export const getRandomPattern = () => {
  const patternKeys = Object.keys(PATTERNS);
  const randomKey = patternKeys[Math.floor(Math.random() * patternKeys.length)];
  return PATTERNS[randomKey];
};
